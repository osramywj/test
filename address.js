const axios = require('axios')
const querystring = require('querystring');

const errorAddress = [];
async function getGeocode(address) {
  const url = 'https://api.map.baidu.com/geocoding/v3/';
  const output = 'json';
  const ak = 'NGP5Z8NpcsIkoTJI4qhIZKwoGdNypN6m'; // 浏览器端密钥
  const encodedAddress = querystring.escape(address); // 编码地址以防止乱码
  const uri = `${url}?address=${encodedAddress}&output=${output}&ak=${ak}`;
  const response = await axios.get(uri)
  if (response.data.result) {
    const location = response.data.result.location;
    const coordinate = location.lng + "," + location.lat;
    return address + "," + coordinate;
  } else {
    errorAddress.push(address);
  }
}
